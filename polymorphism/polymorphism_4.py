# Полиморфизм с операторами
# В Python операторы, такие как +, тоже демонстрируют полиморфизм.
# В зависимости от типов операндов они выполняют разные действия.

# Полиморфизм оператора +
print(10 + 20)  # Складывает числа: 30
print("Hello, " + "world!")  # Конкатенирует строки: Hello, world!
print([1, 2] + [3, 4])  # Объединяет списки: [1, 2, 3, 4]
